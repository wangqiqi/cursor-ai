check_absent() { [[ -e "$1" ]] && { echo "FAIL must not exist: $1"; FAIL=$((FAIL+1)); } || echo "OK  absent $1"; }
check_grep_absent() {
  local dir="$1" pattern="$2"
  if grep -rq --exclude="verify-super-cursor.sh" "$pattern" "$dir" 2>/dev/null; then
    echo "FAIL $dir contains $pattern"
    FAIL=$((FAIL+1))
